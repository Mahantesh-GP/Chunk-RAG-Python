import os
from dotenv import load_dotenv
from datasets import Dataset
import pandas as pd

from ragas import evaluate
from ragas.metrics import (
    faithfulness,          # → Chunk Attribution  (was answer grounded in chunks?)
    context_precision,     # → Chunk Utilization  (were retrieved chunks useful?)
    context_recall,        # → did we retrieve all needed chunks?
    answer_relevancy,      # → is the answer relevant to the question?
)

from langchain_openai import AzureChatOpenAI, AzureOpenAIEmbeddings

load_dotenv()

# ─────────────────────────────────────────────────────────────
# 1. Configure Azure OpenAI models
# ─────────────────────────────────────────────────────────────
def get_azure_models():
    llm = AzureChatOpenAI(
        azure_endpoint=os.getenv("AZURE_OPENAI_ENDPOINT"),
        api_key=os.getenv("AZURE_OPENAI_API_KEY"),
        api_version=os.getenv("AZURE_OPENAI_API_VERSION", "2024-02-01"),
        azure_deployment=os.getenv("AZURE_OPENAI_DEPLOYMENT", "gpt-4o"),
        temperature=0,
    )

    embeddings = AzureOpenAIEmbeddings(
        azure_endpoint=os.getenv("AZURE_OPENAI_ENDPOINT"),
        api_key=os.getenv("AZURE_OPENAI_API_KEY"),
        api_version=os.getenv("AZURE_OPENAI_API_VERSION", "2024-02-01"),
        azure_deployment=os.getenv("AZURE_OPENAI_EMBED_DEPLOYMENT", "text-embedding-ada-002"),
    )

    return llm, embeddings


