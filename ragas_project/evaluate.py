# ─────────────────────────────────────────────────────────────
# 3. Build Hugging Face Dataset (required by Ragas)
# ─────────────────────────────────────────────────────────────
def build_dataset(samples):
    return Dataset.from_dict({
        "question":     [s["question"]     for s in samples],
        "contexts":     [s["contexts"]     for s in samples],
        "answer":       [s["answer"]       for s in samples],
        "ground_truth": [s["ground_truth"] for s in samples],
    })


# ─────────────────────────────────────────────────────────────
# 4. Run Evaluation
# ─────────────────────────────────────────────────────────────
def run_evaluation():
    print("\n" + "="*60)
    print("  Ragas RAG Evaluation — Chunk Attribution & Utilization")
    print("="*60)

    print("\n[1/3] Connecting to Azure OpenAI...")
    llm, embeddings = get_azure_models()
    print("      ✓ Azure OpenAI connected")

    print("\n[2/3] Building dataset...")
    dataset = build_dataset(RAG_SAMPLES)
    print(f"      ✓ {len(RAG_SAMPLES)} samples loaded")

    print("\n[3/3] Running Ragas evaluation (this may take 1-2 mins)...")
    print("      Metrics: faithfulness | context_precision | context_recall | answer_relevancy\n")

    results = evaluate(
        dataset=dataset,
        metrics=[
            faithfulness,       # Chunk Attribution
            context_precision,  # Chunk Utilization
            context_recall,
            answer_relevancy,
        ],
        llm=llm,
        embeddings=embeddings,
        raise_exceptions=False,
    )

    # ─── Print Results ───────────────────────────────────────
    print("\n" + "="*60)
    print("  RESULTS")
    print("="*60)

    print("\n📊 Aggregate Scores:")
    print(f"   Faithfulness    (Chunk Attribution)  : {results['faithfulness']:.4f}  — was answer grounded in retrieved chunks?")
    print(f"   Context Precision (Chunk Utilization): {results['context_precision']:.4f}  — were retrieved chunks actually useful?")
    print(f"   Context Recall                       : {results['context_recall']:.4f}  — were all needed chunks retrieved?")
    print(f"   Answer Relevancy                     : {results['answer_relevancy']:.4f}  — was the answer relevant to the question?")

    # Per-sample breakdown
    df = results.to_pandas()
    print("\n📋 Per-Sample Breakdown:")
    pd.set_option("display.max_colwidth", 60)
    pd.set_option("display.float_format", "{:.4f}".format)
    print(df[["question", "faithfulness", "context_precision", "context_recall", "answer_relevancy"]].to_string(index=True))

    # Save to CSV
    output_path = "ragas_results.csv"
    df.to_csv(output_path, index=False)
    print(f"\n✅ Full results saved to: {output_path}")

    # Score interpretation
    print("\n📖 Score Guide:")
    print("   0.0 - 0.4  → Poor   | chunks not being used / answer hallucinated")
    print("   0.4 - 0.7  → Fair   | partial grounding / some irrelevant chunks")
    print("   0.7 - 1.0  → Good   | answer well grounded in retrieved context")

    return results


# ─────────────────────────────────────────────────────────────
# 5. Entry Point
# ─────────────────────────────────────────────────────────────
if __name__ == "__main__":
    run_evaluation()
